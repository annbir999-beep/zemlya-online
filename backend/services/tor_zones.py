# Все ТОР-регионы
TOR_REGIONS = {**TOR_DV_REGIONS, **TOR_MONOTOWN_REGIONS, **SEZ_REGIONS}


def get_tor_info(region_code: str | None) -> dict | None:
    """Возвращает информацию о ТОР для региона или None если регион не в списке."""
    if not region_code:
        return None
    if region_code in TOR_DV_REGIONS:
        return {
            "type": "dv",
            "label": "ТОР · ДФО",
            "description": "Территория опережающего развития Дальнего Востока. "
                           "Льготы резидентам: налог на прибыль 0% первые 5 лет, "
                           "имущество 0%, страховые 7.6%, земля без торгов (ст. 39.33 ЗК РФ).",
        }
    if region_code in TOR_MONOTOWN_REGIONS:
        return {
            "type": "monotown",
            "label": "ТОР",
            "description": "Территория опережающего развития в моногороде "
                           "(ФЗ-473). Льготы: налог на прибыль 0-5%, страховые 7.6%, "
                           "имущество 0%. Резидентство только при регистрации в моногороде.",
        }
    if region_code in SEZ_REGIONS:
        return {
            "type": "sez",
            "label": "СЭЗ",
            "description": "Свободная экономическая зона (Янтарь). "
                           "Льготы: налог на прибыль 0% первые 6 лет, имущество 0%, "
                           "пониженный таможенный режим.",
        }
    return None
