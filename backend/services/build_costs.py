"""
Справочник средней стоимости строительства каркасного дома по регионам РФ.

Базовая цена 2026 года для каркасника с черновой отделкой по утеплённому
контуру + крыша + окна + базовые коммуникации (электрика+водоснабжение,
без газа/канализации) — ~35 000 ₽/м² «по полу». Это приблизительный
ориентир для калькулятора окупаемости, не оценка строительной компании.

Региональные коэффициенты учитывают:
  · стоимость пиломатериала и доставки
  · средние ставки бригад
  · логистику в удалённые регионы

Источники-ориентиры: открытые прайсы крупных каркасных компаний 2025-2026
(ИзбаПлюс, ДомСтрой, КаркасДом и др.), усреднено.

Цена меняется в 1.5-2 раза в зависимости от уровня отделки (от чернового
контура за 35к до «под ключ» с фасадом и санузлом за 70-80к за м²).
"""
from typing import Optional


# Базовая стоимость каркасника, руб/м² жилой площади, "по полу" с черновой отделкой
BASE_COST_PER_SQM = 35_000

# Среднеотделочный (чистовая отделка, окна, двери, тёплый пол) — для UI
MID_COST_PER_SQM = 55_000

# «Под ключ» — премиум (с фасадом, чистовой санузел, чистовая чердачная)
PREMIUM_COST_PER_SQM = 75_000


# Региональные коэффициенты — умножаем базовую цену.
# По умолчанию 1.0 (большинство ЦФО, ПФО, ЮФО — рынок выровнен).
REGION_BUILD_COEFF: dict[str, float] = {
    # Москва и Московская — премиум локация, бригады дороже
    "77": 1.30, "50": 1.20,
    # Санкт-Петербург и Лен.область
    "78": 1.25, "47": 1.15,
    # Север/Дальний Восток — логистика и климат
    "29": 1.20,  # Архангельская
    "11": 1.20,  # Коми
    "51": 1.25,  # Мурманская
    "10": 1.10,  # Карелия
    "83": 1.40,  # НАО
    "86": 1.20,  # ХМАО
    "89": 1.35,  # ЯНАО
    "14": 1.50,  # Якутия
    "75": 1.30,  # Забайкальский
    "27": 1.30,  # Хабаровский
    "25": 1.30,  # Приморский
    "41": 1.40,  # Камчатский
    "49": 1.50,  # Магадан
    "65": 1.35,  # Сахалинская
    "79": 1.45,  # Еврейская АО
    "87": 1.60,  # Чукотский АО
    # Калининград — анклав, дороже доставка
    "39": 1.25,
    # Сибирь — слегка выше
    "24": 1.10,  # Красноярский
    "38": 1.10,  # Иркутская
    "22": 1.05,  # Алтайский
    "70": 1.10,  # Томская
    "54": 1.05,  # Новосибирская
    "55": 1.05,  # Омская
    "42": 1.10,  # Кемеровская
    # Северный Кавказ — ниже среднего, цены на работу пониже
    "07": 0.90, "08": 0.90, "09": 0.90, "15": 0.90, "20": 0.90,
}


def get_build_cost(
    region_code: Optional[str],
    house_area_sqm: float,
    finish_level: str = "mid",  # "rough" | "mid" | "premium"
) -> dict:
    """Возвращает оценку стоимости постройки для конкретного региона.

    Returns:
        {base_cost_per_sqm, regional_cost_per_sqm, total_cost,
         region_coefficient, finish_level}
    """
    base = {
        "rough": BASE_COST_PER_SQM,
        "mid": MID_COST_PER_SQM,
        "premium": PREMIUM_COST_PER_SQM,
    }.get(finish_level, MID_COST_PER_SQM)

    coef = REGION_BUILD_COEFF.get(region_code or "", 1.0)
    cost_per_sqm = base * coef
    total = cost_per_sqm * max(0.0, house_area_sqm)

    return {
        "base_cost_per_sqm": base,
        "regional_cost_per_sqm": int(cost_per_sqm),
        "total_cost": int(total),
        "region_coefficient": coef,
        "finish_level": finish_level,
    }
