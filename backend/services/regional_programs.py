def get_regional_programs(region_code: str) -> dict:
    rc = (region_code or "").zfill(2)
    federal = [p for p in FEDERAL_PROGRAMS if isinstance(p.get("regions"), list) and rc in p["regions"]]
    federal_all = [p for p in FEDERAL_PROGRAMS if p.get("regions") == "Все регионы" or "земск" in p["name"].lower()]

    return {
        "region_specific": REGIONAL_AVAILABILITY.get(rc),
        "federal_programs": federal + federal_all,
        "privileged_categories": PRIVILEGED_CATEGORIES,
        "verified_through": VERIFIED_THROUGH,
    }
