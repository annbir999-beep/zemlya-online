def haversine(lat1: float, lng1: float, lat2: float, lng2: float) -> float:
    """Расстояние в км между двумя точками на сфере (формула гаверсинусов)."""
    R = 6371.0
    lat1, lng1, lat2, lng2 = map(radians, [lat1, lng1, lat2, lng2])
    dlat = lat2 - lat1
    dlng = lng2 - lng1
    a = sin(dlat / 2) ** 2 + cos(lat1) * cos(lat2) * sin(dlng / 2) ** 2
    return 2 * R * asin(sqrt(a))


def find_nearest_city(lat: float, lng: float, region_code: Optional[str] = None) -> Optional[dict]:
    """
    Возвращает ближайший город из справочника.
    Если region_code задан — приоритет городов своего региона (даже если они дальше).
    """
    if not lat or not lng:
        return None

    best = None
    best_dist = float("inf")
    best_in_region = None
    best_in_region_dist = float("inf")

    for rc, name, pop, clat, clng in CITIES:
        d = haversine(lat, lng, clat, clng)
        if d < best_dist:
            best_dist = d
            best = (rc, name, pop, d)
        if region_code and rc == region_code and d < best_in_region_dist:
            best_in_region_dist = d
            best_in_region = (rc, name, pop, d)

    # Если в своём регионе нашли в пределах 200 км — отдаём его
    if best_in_region and best_in_region_dist <= 200:
        rc, name, pop, d = best_in_region
    elif best:
        rc, name, pop, d = best
    else:
        return None

    return {
        "name": name,
        "region_code": rc,
        "population": pop,
        "distance_km": round(d, 1),
    }
