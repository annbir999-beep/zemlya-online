# ── Индекс keyword → rubric_id ─────────────────────────────────────────────────
_keyword_index: list[tuple[str, int]] = []

for rubric in RUBRICS:
    for kw in rubric["keywords"]:
        _keyword_index.append((kw.lower(), rubric["id"]))

# Сортируем по длине keyword — длинные ключевые слова имеют приоритет
_keyword_index.sort(key=lambda x: -len(x[0]))


def normalize_vri_to_rubric(vri_text: str) -> int:
    """
    Определяет ID рубрики по тексту ВРИ.
    Возвращает 40 (Не детализировано) если совпадений не найдено.
    """
    if not vri_text or not vri_text.strip():
        return 40

    text = vri_text.lower().strip()

    for kw, rubric_id in _keyword_index:
        if kw in text:
            return rubric_id

    return 40


def get_rubric_by_id(rubric_id: int) -> Optional[dict]:
    for r in RUBRICS:
        if r["id"] == rubric_id:
            return r
    return None


def get_rubric_name(rubric_id: int) -> str:
    r = get_rubric_by_id(rubric_id)
    return r["name"] if r else "Не детализировано"


